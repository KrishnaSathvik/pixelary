/**
 * sync-curated-prompts.mjs
 * Exports all curated_prompts from Supabase and writes src/data/curated-prompts.ts.
 * Usage: node scripts/sync-curated-prompts.mjs
 */

import { readFileSync, writeFileSync } from "fs";
import { resolve, dirname } from "path";
import { fileURLToPath } from "url";
import { createClient } from "@supabase/supabase-js";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "..");
const OUT_FILE = resolve(ROOT, "src/data/curated-prompts.ts");

// Load .env
const envFile = readFileSync(resolve(ROOT, ".env"), "utf8");
const env = Object.fromEntries(
  envFile
    .split("\n")
    .filter((l) => l && !l.startsWith("#"))
    .map((l) => {
      const eq = l.indexOf("=");
      return [l.slice(0, eq), l.slice(eq + 1).replace(/^["']|["']$/g, "")];
    })
);

const supabase = createClient(
  env.SUPABASE_URL || env.VITE_SUPABASE_URL,
  env.SUPABASE_SERVICE_ROLE_KEY || env.SUPABASE_PUBLISHABLE_KEY || env.VITE_SUPABASE_PUBLISHABLE_KEY
);

// Fetch all curated prompts
const { data, error } = await supabase
  .from("curated_prompts")
  .select("id, title, category, prompt, why_it_works, source, source_creator, source_url, tags")
  .order("category", { ascending: true })
  .order("created_at", { ascending: true });

if (error) {
  console.error("Failed to fetch:", error.message);
  process.exit(1);
}

// Group by category for organized output
const byCategory = new Map();
for (const p of data) {
  const cat = p.category || "Cinematic";
  if (!byCategory.has(cat)) byCategory.set(cat, []);
  byCategory.get(cat).push(p);
}

// Escape backticks and backslashes for template literal safety
function escapeForTS(str) {
  if (!str) return str;
  return str.replace(/\\/g, "\\\\").replace(/`/g, "\\`").replace(/\$/g, "\\$");
}

// Build the TypeScript file
let ts = `/**
 * ${data.length} curated GPT Image 2 prompts, synced from Supabase.
 * Auto-generated by scripts/sync-curated-prompts.mjs — do not edit manually.
 *
 * All categories mapped to Depikt's existing 10:
 *   Posters, Infographics, UI Mockups, Social Posts, Cinematic, Storyboards,
 *   Interior/Food/Fashion, Visual Summaries, Image Edits, Open-Ended Creative
 */

export type CuratedPrompt = {
  id: string;
  title: string;
  category: string;
  prompt: string;
  why_it_works?: string;
  source: 'curated';
  source_creator?: string;
  source_url?: string;
  tags: string[];
};

export const curatedPrompts: CuratedPrompt[] = [\n`;

const categoryOrder = [
  "Cinematic",
  "Posters",
  "Infographics",
  "UI Mockups",
  "Social Posts",
  "Storyboards",
  "Interior/Food/Fashion",
  "Visual Summaries",
  "Image Edits",
  "Open-Ended Creative",
];

for (const cat of categoryOrder) {
  const prompts = byCategory.get(cat);
  if (!prompts || prompts.length === 0) continue;

  ts += `  // ============================================================\n`;
  ts += `  // ${cat.toUpperCase()} (${prompts.length})\n`;
  ts += `  // ============================================================\n`;

  for (const p of prompts) {
    ts += `  {\n`;
    ts += `    id: '${p.id}',\n`;
    ts += `    title: '${escapeForTS(p.title).replace(/'/g, "\\'")}',\n`;
    ts += `    category: '${p.category}',\n`;
    ts += `    prompt: \`${escapeForTS(p.prompt)}\`,\n`;
    if (p.why_it_works) {
      ts += `    why_it_works: \`${escapeForTS(p.why_it_works)}\`,\n`;
    }
    ts += `    source: 'curated',\n`;
    if (p.source_creator) {
      ts += `    source_creator: '${escapeForTS(p.source_creator).replace(/'/g, "\\'")}',\n`;
    }
    if (p.source_url) {
      ts += `    source_url: '${escapeForTS(p.source_url)}',\n`;
    }
    ts += `    tags: [${(p.tags || []).map((t) => `'${escapeForTS(t).replace(/'/g, "\\'")}'`).join(", ")}],\n`;
    ts += `  },\n`;
  }
}

ts += `];\n`;

writeFileSync(OUT_FILE, ts, "utf8");

// Summary
console.log(`Synced ${data.length} prompts to ${OUT_FILE}`);
for (const cat of categoryOrder) {
  const count = byCategory.get(cat)?.length || 0;
  console.log(`  ${cat}: ${count}`);
}
